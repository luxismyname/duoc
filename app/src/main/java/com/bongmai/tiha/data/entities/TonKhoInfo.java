package com.bongmai.tiha.data.entities;

public class TonKhoInfo {
    private String MaHang;
    private String TenHang;
    private double DauKy;
    private double Nhap;
    private double Xuat;
    private double ChuyenDen;
    private double ChuyenDi;
    private double CuoiKy;

    public String getMaHang() {
        return MaHang;
    }

    public void setMaHang(String maHang) {
        MaHang = maHang;
    }

    public String getTenHang() {
        return TenHang;
    }

    public void setTenHang(String tenHang) {
        TenHang = tenHang;
    }

    public double getDauKy() {
        return DauKy;
    }

    public void setDauKy(double dauKy) {
        DauKy = dauKy;
    }

    public double getNhap() {
        return Nhap;
    }

    public void setNhap(double nhap) {
        Nhap = nhap;
    }

    public double getXuat() {
        return Xuat;
    }

    public void setXuat(double xuat) {
        Xuat = xuat;
    }

    public double getChuyenDen() {
        return ChuyenDen;
    }

    public void setChuyenDen(double chuyenDen) {
        ChuyenDen = chuyenDen;
    }

    public double getChuyenDi() {
        return ChuyenDi;
    }

    public void setChuyenDi(double chuyenDi) {
        ChuyenDi = chuyenDi;
    }

    public double getCuoiKy() {
        return CuoiKy;
    }

    public void setCuoiKy(double cuoiKy) {
        CuoiKy = cuoiKy;
    }
}
